/* This program aims to use the function "printf()"
   by outputting my name in different ways. */
#include <stdio.h>

int main(void)
{
	printf("Fang Kun\n");	// output my name in one row
	printf("Fang\nKun\n");	// output my name in two seperate rows
	printf("Fang");		// the third statement
	printf("Kun\n");		// the fourth statement
	return 0;
}
